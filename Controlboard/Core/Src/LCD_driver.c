/****
*	功能：设置屏幕的位置
*	输入：无
*	输出：无
****/
static void LCD_SetPos(uint8_t _ucPage,uint8_t _ucColumn)
{
	LCD_CS_L();

//	_ucPage -= 1;
	LCD_Write(REG_CMD,0xb0+_ucPage);
	LCD_Write(REG_CMD,((_ucColumn>>4) & 0x0f) + 0x10);
	LCD_Write(REG_CMD,(_ucColumn & 0x0f));
}

