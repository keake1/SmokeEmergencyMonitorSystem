void LCD_Clear(void)
{
	uint8_t i,j;
	LCD_CS_L();
	for(i=0; i<9; i++)
	{
		LCD_SetPos(i,1);
		for(j=0; j<132; j++)
		{
			LCD_Write(REG_DATA,0x00);
		}
	}
	LCD_CS_H();
}

