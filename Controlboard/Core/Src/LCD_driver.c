void LCD_Dis_String(int  x,int   y,   char disMode, char  *string)
{
    int index=0;
    uint8_t   c= 0         ;
  uint8_t  	buffer[128]  ; 
    while (string[index]>0)
    {		
        c = string[index] ;
        switch(disMode)
        {
            case 10:
            case  0:
                        if(c < 0x80)  //  ASCII 字符 
                                {
                                     memcpy((char *)buffer,ASCII5X7Code[c- ' '],6);
                                     if(disMode == 10)  {
                                       int i;
                                         for(i=0;i<6;i++) buffer[i] = ~buffer[i];
                                     }
                                     LCD_Set_Buffer(x,y,buffer,6);
                                     x += 6;	
                                     if(x>128-6){x=0;y=y+1;}	// 自动换行
                              } 
                        break;
                                
            case 11:					
            case 1 :
                if(c < 0x80)  //  ASCII 字符 
                                {
                                        c -= ' ' ;
                                      memcpy((char *)buffer,ASCII8X16Code[c],16);
                                      if(disMode == 11)  {
                                       int i;
                                         for(i=0;i<16;i++) buffer[i] = ~buffer[i];
                                     }
                                        LCD_Set_Buffer(x,y,buffer,8);						
                                        LCD_Set_Buffer(x,y+1,buffer+8,8);
                                        
                      }
                      x += 8;     //  自动换行
                      if(x>120)
                                {
                                     x=0  ;
                                     y=y+2;
                                } 
                    break;
#if ASCII12X24Code_EN != 0 								
      case 12:								
            case 2:
                if(c < 0x80)  //  ASCII 字符 
                                {
                                        c -= ' ' ;
                                      memcpy(buffer,ASCII12X24Code[c],36);
                                      if(disMode == 12)  {
                                       int i;
                                         for(i=0;i<36;i++) buffer[i] = ~buffer[i];
                                      }
                                        LCD_Set_Buffer(x,y,buffer,12);										
                                        LCD_Set_Buffer(x,y+1,buffer+12,12);
                    LCD_Set_Buffer(x,y+2,buffer+24,12);
                                                        
                      }
                      x += 12;     //  自动换行
                      if(x>116)
                                {
                                     x=0  ;
                                     y=y+3;
                                } 
                    break;  
#endif
                                
#if ASCII12X24Code_EN != 0
            case 13:					   
            case 3:
                if(c < 0x80)  //  ASCII 字符 
                                {
                                        c -= ' ' ;
                                      memcpy(buffer,ASCII16X32Code[c],64);
                                      if(disMode == 13)  {
                                       int i;
                                         for(i=0;i<64;i++) buffer[i] = ~buffer[i];
                                       }
                                        LCD_Set_Buffer(x,y,buffer,16);										
                                        LCD_Set_Buffer(x,y+1,buffer+16,16);
                    LCD_Set_Buffer(x,y+2,buffer+32,16);
                                        LCD_Set_Buffer(x,y+3,buffer+48,16);
                      }
                      x += 16;     //  自动换行
                      if(x>112)
                                {
                                     x=0  ;
                                     y=y+4;
                                } 
                    break; 
#endif								
      default:  
                    break;  
        }
        
        if(y > 7)  break;	  //  显示超行，退出
        index++;
    }
}


