/****
*	功能：显示单个8*16字符
*	输入：无
*	输出：无
****/
void LCD_DispChar8x16(uint8_t _ucPage,uint8_t _ucColumn,uint8_t *p)
{
	uint8_t i,j;
	LCD_CS_L();

	for(j=0; j<2; j++)
	{
        
		LCD_SetPos(_ucPage+j,_ucColumn);
		for(i=0; i<8; i++)
		{
			LCD_Write(REG_DATA,*p);
			p++;
		}
		
	}
	LCD_CS_H();
}
